// SIZE OF THE PRIMITIVE DATA TYPES

#include<stdio.h>

int main()
{
    printf("\ncharacter stores %d bytes in the memory\n", sizeof(char));
    printf("\ninteger stores %d bytes in the memory\n", sizeof(int));
    printf("\nfloat stores %d bytes in the memory\n", sizeof(float));
    printf("\ndouble stores %d bytes in the memory\n", sizeof(double));
    printf("\nvoid stores %d bytes in the memory\n", sizeof(void));

    return 0;
}